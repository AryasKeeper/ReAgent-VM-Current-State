class GlobalAgentMonitor:
    """Global monitoring system for all agents."""
    
    def __init__(self):
        self.agent_monitors: Dict[str, AgentMonitor] = {}
        self.system_health_check_interval = 60  # seconds
        self.health_check_thread: Optional[threading.Thread] = None
        self.running = False
        
    def get_agent_monitor(self, agent_name: str) -> AgentMonitor:
        """Get or create an agent monitor."""
        if agent_name not in self.agent_monitors:
            self.agent_monitors[agent_name] = AgentMonitor(agent_name)
        return self.agent_monitors[agent_name]
    
    def start_global_monitoring(self):
        """Start global monitoring system."""
        self.running = True
        
        def health_check_loop():
            while self.running:
                try:
                    self._update_global_metrics()
                    time.sleep(self.system_health_check_interval)
                except Exception as e:
                    logger.error("Error in global monitoring loop", error=str(e), exc_info=True)
        
        self.health_check_thread = threading.Thread(target=health_check_loop, daemon=True)
        self.health_check_thread.start()
        
        logger.info("Started global agent monitoring system")
    
    def stop_global_monitoring(self):
        """Stop global monitoring system."""
        self.running = False
        if self.health_check_thread:
            self.health_check_thread.join(timeout=5)
        
        logger.info("Stopped global agent monitoring system")
    
    def _update_global_metrics(self):
        """Update global monitoring metrics."""
        total_active_executions = 0
        total_agents = len(self.agent_monitors)
        
        for agent_name, monitor in self.agent_monitors.items():
            performance = monitor.get_current_performance()
            total_active_executions += performance["active_executions"]
            
            # Update global metrics
            agent_task_queue_length.labels(
                agent_name=agent_name,
                priority="normal"
            ).set(performance["current_queue_length"])
        
        logger.debug(
            "Updated global agent metrics",
            total_agents=total_agents,
            total_active_executions=total_active_executions
        )
    
    def get_system_overview(self) -> Dict[str, Any]:
        """Get overview of all agent performance."""
        overview = {
            "timestamp": time.time(),
            "total_agents": len(self.agent_monitors),
            "agents": {}
        }
        
        for agent_name, monitor in self.agent_monitors.items():
            overview["agents"][agent_name] = monitor.get_current_performance()
        
        return overview
    
    def get_performance_summary(self, time_window_minutes: int = 60) -> Dict[str, Any]:
        """Get performance summary for all agents over a time window."""
        cutoff_time = time.time() - (time_window_minutes * 60)
        
        summary = {
            "time_window_minutes": time_window_minutes,
            "summary_timestamp": time.time(),
            "agents": {}
        }
        
        for agent_name, monitor in self.agent_monitors.items():
            # Filter executions within time window
            recent_executions = [
                exec for exec in monitor.execution_history
                if exec.start_time >= cutoff_time
            ]
            
            if recent_executions:
                success_count = sum(1 for exec in recent_executions if exec.status == "completed")
                total_duration = sum(exec.duration_seconds for exec in recent_executions if exec.duration_seconds)
                
                summary["agents"][agent_name] = {
                    "executions": len(recent_executions),
                    "success_rate": success_count / len(recent_executions),
                    "average_duration": total_duration / len(recent_executions),
                    "total_business_value": sum(
                        sum(exec.business_outcomes.values()) if exec.business_outcomes else 0
                        for exec in recent_executions
                    )
                }
            else:
                summary["agents"][agent_name] = {
                    "executions": 0,
                    "success_rate": 0,
                    "average_duration": 0,
                    "total_business_value": 0
                }
        
        return summary

# Global instance
